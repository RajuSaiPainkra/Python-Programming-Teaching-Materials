# Sentances Cases in python 

# 1. PascalCase
# 2. camelCase
# 3. snake_case
# 4. kebab-case 

## Pascal Case
# Every word starts with a capital letter, no spaces or underscores
# Example: HelloWorld, MyVariableName

## Camel Case
# Similar to Pascal Case, but the first word starts with a lowercase letter
# Example: helloWorld, myVariableName

## Snake Case
# Words are separated by underscores, all lowercase
# Example: hello_world, my_variable_name

## Kebab Case
# Words are separated by hyphens, all lowercase
# Example: hello-world, my-variable-name